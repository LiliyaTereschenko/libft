#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char	*d;
	char	*s;
	size_t	i;

	i = 0;
	d = dest;
	s = (char *)src;
	while (s[i] != '\0' && i < n)
	{
		d[i] = s[i];
		i++;
	}
	while (!s[i] && d[i] != '\0')
		i++;
	d[i] = '\0';
	return (d);
}
